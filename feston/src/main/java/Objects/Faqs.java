package Objects;

public class Faqs {

	private String faqId;
	private String question_lt;
	private String question_en;
	private String answer_lt;
	private String answer_en;
	private String position;

	public String getFaqId() {
		return faqId;
	}

	public void setFaqId(String faqId) {
		this.faqId = faqId;
	}

	public String getQuestion_lt() {
		return question_lt;
	}

	public void setQuestion_lt(String question_lt) {
		this.question_lt = question_lt;
	}

	public String getQuestion_en() {
		return question_en;
	}

	public void setQuestion_en(String question_en) {
		this.question_en = question_en;
	}

	public String getAnswer_lt() {
		return answer_lt;
	}

	public void setAnswer_lt(String answer_lt) {
		this.answer_lt = answer_lt;
	}

	public String getAnswer_en() {
		return answer_en;
	}

	public void setAnswer_en(String answer_en) {
		this.answer_en = answer_en;
	}

	public String getPosition() {
		return position;
	}

	public void setPosition(String position) {
		this.position = position;
	}

}
